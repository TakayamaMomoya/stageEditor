#include "UI.h"
#include "UIManager.h"

//=====================================================
// 優先順位を決めるコンストラクタ
//=====================================================
CUI::CUI(int nPriority) : CObject2D(nPriority)
{

}

//=====================================================
// デストラクタ
//=====================================================
CUI::~CUI()
{

}

//=====================================================
// 生成処理
//=====================================================
CUI *CUI::Create(void)
{
	CUI *pUI = nullptr;

	if (pUI == nullptr)
	{
		// インスタンス生成
		pUI = new CUI;

		if (pUI != nullptr)
		{
			// 初期化処理
			pUI->Init();
		}
	}

	return pUI;
}

//=====================================================
// 初期化
//=====================================================
HRESULT CUI::Init(void)
{
	CObject2D::Init();

	return S_OK;
}

//=====================================================
// 終了処理
//=====================================================
void CUI::Uninit(void)
{
	CObject2D::Uninit();
}

//=====================================================
// 更新処理
//=====================================================
void CUI::Update(void)
{
	CObject2D::Update();
}

//=====================================================
// 描画
//=====================================================
void CUI::Draw(void)
{
	bool bDisp = true;

	CUIManager *pUIManager = CUIManager::GetInstance();

	if (pUIManager != nullptr)
	{// 描画するかの取得
		bDisp = pUIManager->IsDisp();
	}

	if (bDisp)
	{
		CObject2D::Draw();
	}
}